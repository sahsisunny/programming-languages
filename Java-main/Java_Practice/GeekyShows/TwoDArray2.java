class TwoDArray2{
	public static void main(String args[])
	{
		int num[][]={{11,22,33,44},{55,66,77,77}};
	
		System.out.println("num[0][0]="+num[0][0]);
		System.out.println("num[0][1]="+num[0][1]);
		System.out.println("num[0][2]="+num[0][2]);
		System.out.println("num[0][3]="+num[0][3]);
		System.out.println("num[0][03]="+num[0][03]);
	}
}
			