class Ini2DArrayWith2DArray{
	public static void main(String args[])
	{
		int num[][]={{11,22,33,44},{55,66,77,77}};
		int mark[][];
		mark=num;
		System.out.println("mark[0][0]="+mark[0][0]);
		System.out.println("mark[0][1]="+mark[0][1]);
		System.out.println("mark[0][2]="+mark[0][2]);
		System.out.println("num[0][3]="+num[0][3]);
		System.out.println("num[0][03]="+num[0][03]);
	}
}
			