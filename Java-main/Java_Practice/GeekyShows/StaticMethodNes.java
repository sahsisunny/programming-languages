class StaticMethodNes
{
	public static void main(String args[])		//Static Method
	{
		disp();
		play();
	}
	static void disp()			//Static Method
	{
		int x=100;
		System.out.println("Value of A= "+x);
	}
	static void play()			//Static Method
	{
		int z=200;
		System.out.println("Value of B= "+z);
	}
}