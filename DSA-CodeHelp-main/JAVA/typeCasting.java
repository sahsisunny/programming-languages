// Typecasting in java
public class typeCasting {
     public static void main(String[] args) {
          System.out.println("Implicit Typecasting");
          int a = 10;
          double b = a;
          System.out.println(b);

          System.out.println("Explicit Typecasting");
          double c = 10.5;
          int d = (int) c;
          System.out.println(d);

     }

}
