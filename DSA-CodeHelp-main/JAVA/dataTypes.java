// Datatypes in Java
public class dataTypes {
     public static void main(String[] args) {
          System.out.println("Primitive Datatypes in Java");
          System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
          System.out.println("byte: " + Byte.MIN_VALUE + " to " + Byte.MAX_VALUE);
          System.out.println("short: " + Short.MIN_VALUE + " to " + Short.MAX_VALUE);
          System.out.println("int: " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);
          System.out.println("long: " + Long.MIN_VALUE + " to " + Long.MAX_VALUE);
          System.out.println("float: " + Float.MIN_VALUE + " to " + Float.MAX_VALUE);
          System.out.println("double: " + Double.MIN_VALUE + " to " + Double.MAX_VALUE);
          System.out.println("char: " + Character.MIN_VALUE + " to " + Character.MAX_VALUE);
          System.out.println("boolean: " + Boolean.FALSE + " to " + Boolean.TRUE);
          
          System.out.println();

          System.out.println("Non-Primitive Datatypes in Java");
          System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
          System.out.println("String: " + "String is a sequence of characters");
          System.out.println("Array: " + "Array is a collection of similar type of elements");
          System.out.println("Class: " + "Class is a collection of data members and methods");
          System.out.println("Interface: " + "Interface is a collection of abstract methods");
          System.out.println("Enum: " + "Enum is a collection of constants");

     }
}
