# Comparision two arrays using numpy

a = array([100, 200, 3, 400, 500])
b = array([10, 200, 30, 40, 500])

# Less than
print("Less than")
c = a < b
for i in c:
    print(i)

# Greater than
print("Greater than")
c = a > b
for i in c:
    print(i)

# Less than equal to
print("Less than equal to")
c = a <= b
for i in c:
    print(i)

# Greater than equal to
print("Greater than equal to")
c = a >= b
for i in c:
    print(i)

# Equal to
print("Equal to")
c = a == b
for i in c:
    print(i)

# Not equal to
print("Not equal to")
c = a == b
for i in c:
    print(i)
