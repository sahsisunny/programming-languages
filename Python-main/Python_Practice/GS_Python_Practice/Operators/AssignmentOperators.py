# Assignment Operators

print("Addition m=15, m+=10")
m = 15
m += 10
print(m)

print("Subtraction m=15, m-=10")
m = 15
m -= 10
print(m)

print("Multiplication m=15, m*=10")
m = 15
m *= 10
print(m)

print("Division m=15, m/=10")
m = 15
m /= 10
print(m)

print("Modulus m=15, m%=10")
m = 15
m %= 10
print(m)

print("Exponent m=15, m**=2")
m = 15
m **= 2
print(m)

print("Floor Division m=15, m//=10")
m = 15
m //= 10
print(m)

print("Floor Division m=-15, m//=10")
m = -15
m //= 10
print(m)
