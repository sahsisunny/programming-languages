a = 5
b = 2
print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a % b)  # Modulus
print(a ** b)  # Exponent
print(a // b)  # Integer/Floor Division results in round figure
print(-a // b)  # Integer/Floor Division results in round figure
