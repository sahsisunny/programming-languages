# Membership Operators

# there are two types of Membership Operators
#	1. in
#	2. not in

st = "Welcome to Shield Squads"

print("\tin Operator")
print("to" in st)  # True
print("To" in st)  # False
print("the" in st)  # False

print("\tin Operator")
print("to" not in st)  # False
print("the" not in st)  # True
