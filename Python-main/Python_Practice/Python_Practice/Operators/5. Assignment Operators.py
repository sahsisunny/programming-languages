# Bitwise Operators
n = int(input("Enter the number: "))

print("Assign(=): n = ", n)

n += 5
print("Add and assign(+=): n = ", n)

n -= 5
print("Subtract and assign(-=): n = ", n)

n *= 4
print("Multiply and assign(*=): n = ", n)

n /= 2
print("Divide and assign(/=): n = ", n)

n %= 3
print("Modulus and assign(%=): n = ", n)

n **= 4
print("Exponentiation and assign(**=): n = ", n)

n //= 2
print("Floor divide and assign(//=): n = ", n)
