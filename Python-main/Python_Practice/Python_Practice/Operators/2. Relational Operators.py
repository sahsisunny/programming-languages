# Relational Operators
x = int(input("Enter the 1st number: "))
y = int(input("Enter the 2nd number: "))

# 1. Greater than: True if left operand is greater than the right
print(x, "Greater than", y, "=", x > y)

# 2. Less than: True if left operand is less than the right
print(x, "Less than", y, "=", x < y)

# 3. Equal to: True if both operands are equal
print(x, "Equal to", y, "=", x == y)

# 4. Not equal to - True if operands are not equal
print(x, "Not equal to", y, "=", x != y)

# 5. Greater than or equal to: True if left operand is greater than or equal to the right
print(x, "Greater than or equal to", y, "=", x >= y)

# 6. Less than or equal to: True if left operand is less than or equal to the right
print(x, "Less than or equal to", y, "=", x <= y)
