import java.math.*;

public class question4 {
     public static void main(String[] args) {
          double x = 12.43, y = 13.55;

          // Math.min() - returns the smaller of two numbers
          System.out.println("Math.min(" + x + ", " + y + "): " + Math.min(x, y));

          // Math.max() - returns the larger of two numbers
          System.out.println("Math.max(" + x + ", " + y + "): " + Math.max(x, y));

          // Math.sqrt() - returns the square root of a number
          System.out.println("Math.sqrt(" + x + "): " + Math.sqrt(x));

          // Math.pow() - returns the value of the first argument raised to the power
          System.out.println("Math.pow(" + x + ", " + y + "): " + Math.pow(x, y));

          // Math.avg() - it is not available in Java

          // Math.abs() - returns the absolute value of a number
          System.out.println("Math.abs(" + x + "): " + Math.abs(x));

     }
}
